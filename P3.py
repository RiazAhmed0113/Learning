# methods/functions
name = 'riaz'

print(len(name))  # print the length of the string
print(name.find("i"))   # Find the index position of string variable
print(name.capitalize())    # Capitalize the first character of the string
print(name.upper())     # ALL CAPITALS
print(name.lower())     # all lower case
print(name.isdigit())   # return bool value if string is a digit or not/
print(name.isalpha())   # return bool value if string is all alphabets
print(name.count('z'))  # count how many characters are in the string variable
print(name.replace('a', 'z'))   # replace character in a string
print(name*3)   # repeat print (saves time)
